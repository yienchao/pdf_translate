examples = []

for idx, (hash_key, french_text) in enumerate(pending_data.items(), 1):
    english_text = translate_text(french_text, translations_dict, word_patterns)
    translations[hash_key] = english_text

    # Store first 3 examples
    if len(examples) < 3:
        examples.append({
            "hash": hash_key,
            "french": french_text,
            "english": english_text
        })

    if idx % 100 == 0:
        print(f"  Processed {idx}/{len(pending_data)} items...")

