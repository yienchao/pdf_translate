def translate_text(french_text, dict_trans, patterns):
    """Translate French text using dictionary and word patterns"""
    # First, check if exact match in dictionary
    if french_text in dict_trans:
        return dict_trans[french_text]

    # Try to translate word by word
    result = french_text
    sorted_patterns = sorted(patterns.items(), key=lambda x: len(x[0]), reverse=True)

    for french_word, english_word in sorted_patterns:
        result = result.replace(french_word, english_word)

    return result

